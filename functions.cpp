#include <iostream>
#include "functions.h"
/**
 * Implementation of 4 basic aritmetic operations
 * @param  x real number
 * @param  y real number
 * @return   Result of x "op" y
 */
double sum(double x, double y){
	return x + y;
}
double sub(double x, double y){
	return x - y;
}
double mul(double x, double y){
	return x * y;
}
double divi(double x, double y){
	return x / y;
}